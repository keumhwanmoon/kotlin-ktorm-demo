CREATE TABLE IF NOT EXISTS USERS (
    ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    LOGIN_ID VARCHAR(255) NOT NULL UNIQUE,
    USER_NAME VARCHAR(255),
    PICTURE VARCHAR(100),
    PHONE_NUMBER VARCHAR(100),
    MOBILE_PHONE_NUMBER VARCHAR(100),
    EMAIL VARCHAR(100),
    POSITION_ID VARCHAR(32),
    DUTY_ID VARCHAR(32),
    PWD VARCHAR(60),
    PWD_CHANGE_YN CHAR(1),
    PWD_CHANGED_AT TIMESTAMP,
    LOGIN_FAIL_CNT INT DEFAULT 0,
    LOCK_YN CHAR(1) NOT NULL,
    ENABLE_YN CHAR(1) NOT NULL,
    EXPR_YN CHAR(1) NOT NULL,
    CREATED_AT TIMESTAMP,
    CREATE_LOGIN_ID VARCHAR(32),
    LAST_UPDATED_AT TIMESTAMP,
    LAST_UPDATE_LOGIN_ID VARCHAR(32)
);
